# Initial function

def greet():
    print("Hello!")
    print("How are you doing?")
    print("What's going on?")

greet()    

# Functions that allows for inputs

def greet_with_name(name):
    print(f"Hello! {name}")
    print(f"How are you doing? {name}")
    print(f"What's going on? {name}")

greet_with_name("Vinicius")    